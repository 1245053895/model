package com.xd.zt.service.sso;

import com.xd.zt.mapper.sso.MySessionCookieConfig;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;

@Component
@ConfigurationProperties(prefix = "server.servlet.session.cookie")
public class MyDefaultSessionCookieConfig implements MySessionCookieConfig {
    private String name = "ZNFX-SESSION-ID";
    private String path;
    private String domain;
    private String comment;
    private int maxAge = -1;
    private String domainPattern;
    private String jvmRoute;
    private boolean httpOnly = true;
    private boolean secure = false;
    private boolean useBase64Encoding = false;
    @Override     public String getDomainPattern() {
        return domainPattern;
    }
    @Override     public void setDomainPattern(String domainPattern) {
        this.domainPattern = domainPattern;
    }
    @Override     public String getJvmRoute() {
        return jvmRoute;
    }
    @Override     public void setJvmRoute(String jvmRoute) {
        this.jvmRoute = jvmRoute;
    }
    @Override     public boolean isUseBase64Encoding() {
        return useBase64Encoding;
    }
    @Override     public void setUseBase64Encoding(boolean useBase64Encoding) {
        this.useBase64Encoding = useBase64Encoding;
    }
    @Override     public String getName() {
        return name;
    }
    @Override     public void setName(String name) {
        this.name = name;
    }
    @Override     public String getPath() {
        return path;
    }
    @Override
    public void setPath(String path) {
        this.path = path;
    }
    @Override     public String getDomain() {
        return domain;
    }
    @Override     public void setDomain(String domain) {
        this.domain = domain;
    }
    @Override     public String getComment() {
        return comment;
    }
    @Override     public void setComment(String comment) {
        this.comment = comment;
    }
    @Override     public boolean isHttpOnly() {
        return httpOnly;
    }
    @Override     public void setHttpOnly(boolean httpOnly) {
        this.httpOnly = httpOnly;
    }
    @Override     public boolean isSecure() {
        return secure;
    }
    @Override     public void setSecure(boolean secure) {
        this.secure = secure;
    }
    @Override     public int getMaxAge() {
        return maxAge;
    }
    @Override     public void setMaxAge(int maxAge) {
        this.maxAge = maxAge;
    }
}