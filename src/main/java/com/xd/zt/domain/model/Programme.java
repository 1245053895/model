package com.xd.zt.domain.model;

public class Programme {
    Integer programmeid;
    String programmename;
    String programmetype;
    String programmedescribe;
    String programmelabel;
    String programmetime;
    String username;

    public Integer getProgrammeid() {
        return programmeid;
    }

    public void setProgrammeid(Integer programmeid) {
        this.programmeid = programmeid;
    }

    public String getProgrammename() {
        return programmename;
    }

    public void setProgrammename(String programmename) {
        this.programmename = programmename;
    }

    public String getProgrammetype() {
        return programmetype;
    }

    public void setProgrammetype(String programmetype) {
        this.programmetype = programmetype;
    }

    public String getProgrammedescribe() {
        return programmedescribe;
    }

    public void setProgrammedescribe(String programmedescribe) {
        this.programmedescribe = programmedescribe;
    }

    public String getProgrammelabel() {
        return programmelabel;
    }

    public void setProgrammelabel(String programmelabel) {
        this.programmelabel = programmelabel;
    }

    public String getProgrammetime() {
        return programmetime;
    }

    public void setProgrammetime(String programmetime) {
        this.programmetime = programmetime;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
