package com.xd.zt.domain.analyse;

public class AnalyseSource {
    Integer analysesourceid;
    String analysesourcename;
    String analysesourcetime;
    String analysesourcesize;
    String analysesourcepath;
    Integer analysemodelid;

    public Integer getAnalysesourceid() {
        return analysesourceid;
    }

    public void setAnalysesourceid(Integer analysesourceid) {
        this.analysesourceid = analysesourceid;
    }

    public String getAnalysesourcename() {
        return analysesourcename;
    }

    public void setAnalysesourcename(String analysesourcename) {
        this.analysesourcename = analysesourcename;
    }

    public String getAnalysesourcetime() {
        return analysesourcetime;
    }

    public void setAnalysesourcetime(String analysesourcetime) {
        this.analysesourcetime = analysesourcetime;
    }

    public String getAnalysesourcesize() {
        return analysesourcesize;
    }

    public void setAnalysesourcesize(String analysesourcesize) {
        this.analysesourcesize = analysesourcesize;
    }

    public String getAnalysesourcepath() {
        return analysesourcepath;
    }

    public void setAnalysesourcepath(String analysesourcepath) {
        this.analysesourcepath = analysesourcepath;
    }

    public Integer getAnalysemodelid() {
        return analysemodelid;
    }

    public void setAnalysemodelid(Integer analysemodelid) {
        this.analysemodelid = analysemodelid;
    }
}
